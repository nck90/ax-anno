export interface AuctionHeader {
  court: string;
  department: string;
  saleDate: string;
  saleDecisionDate: string;
  salePlace: string;
  judge: string;
  publishDate: string;
}

export interface AuctionProperty {
  propertyNumber: string;
  location: string;
  usage: string;
  appraisalPrice: string;
  minimumPrice: string;
  remarks: string;
}

export interface AuctionCase {
  caseNumber: string;
  duplicateInfo?: string;
  properties: AuctionProperty[];
}

export interface AuctionCategory {
  name: string;
  items: AuctionCase[];
}

export interface EditedAuction {
  header: AuctionHeader;
  categories: AuctionCategory[];
}
